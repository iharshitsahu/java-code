class SumOfTwoNumber {
    public static int SumNum(int a, int b) {
        int sum =a+b;
        return sum;
    }

    public static void main(String args[]) {
        int a=2060;
        int b=5999;
        int sum = SumNum(a, b);
        System.out.println("Sum of 2 numbers:-"+sum);
        
    }
}